var soma = 5 + 3; // var (variavel) '+' simbolo de somatória
var subtracao = 10 - 4; // '-' simbolo de subtrção
var multiplicacao = 2 * 6; // '*' simbolo de multiplicação
var divisao = 10 / 3; // '/' simbolo de divisão
var modulo = 10 % 3; // '%' simbolo de modulos ou porcentagens

console.log("Resultado da soma: " + soma); // comando de soma
console.log("Resultado da subtração:" + subtracao); // comando de subtração
console.log("Resultado dqa soma: " + multiplicacao); // comando de multiplição
console.log("Resultado da subtração:" + divisao); // comando de subtrção
console.log("Resultado do módulo (resto da divisão): " + modulo); // comando de divisão